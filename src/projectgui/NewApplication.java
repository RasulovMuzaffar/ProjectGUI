//                for (int qq = 0; qq < i; qq++) {
                double x1 = (x0 + Math.cos(Math.toRadians(180 - alfa)) * (100 * dH));
                double y1 = (y0 + Math.sin(Math.toRadians(180 - alfa)) * (100 * dH));

                lineBuffer = new Line2D.Double(x0, y0, x1, y1);
