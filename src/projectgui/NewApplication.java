    private void DrawAll() {
        for (int i = 0; i < lineContainer.size(); i++) {
            g.draw(lineContainer.get(i));
        }
        for (int i = 0; i < circContainer.size(); i++) {
            g.draw(circContainer.get(i));
        }
    }

    private void DrawLineHor(double x0, double y0) {
        for (int l = 0; l < 10; l++) {
            double x1 = (x0 + Math.cos(Math.toRadians(0)) * (100 * dH));
            double y1 = (y0 + Math.sin(Math.toRadians(0)) * (100 * dH));
            lineBuffer = new Line2D.Double(x0, y0, x1, y1);
            lineContainer.add(lineBuffer);

            x0 = x1;
            y0 = y1;
//            System.out.println(x0 + " - " + y0);
        }
    }

